# List in Python

names = ["Aaditya", "Kartik", "Nikhil", "Ram", "Ankush"]
print(names)
print(names[:])     # can also get complete list

# to print name of specific index
print(names[0])     # print from head of list
print(names[-1])    # print from tail of list
print(names[2:])    # prints from index no. 2 to tail
print(names[0:2])   # prints from index no. 0 to 2 except 2
print(names[2:4])
print(names[1])
